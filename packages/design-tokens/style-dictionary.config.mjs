// TypeScript const object with "as const"
StyleDictionary.registerFormat({
  name: 'relay/ts-tokens',
  format: async ({ dictionary, file, options }) => {
    const header = await fileHeader({ file });

    // Build a nested object from the flat token list
    function buildNested(tokens) {
      const result = {};
      for (const token of tokens) {
        const pathParts = token.path;
        let current = result;
        for (let i = 0; i < pathParts.length - 1; i++) {
          const key = pathParts[i];
          if (!(key in current) || typeof current[key] !== 'object') {
            current[key] = {};
          }
          current = current[key];
        }
        const lastKey = pathParts[pathParts.length - 1];
        const v = token.$value ?? token.value;
        // For shadow objects, keep as-is; for primitives, use the value
        current[lastKey] = typeof v === 'object' && v !== null ? v : v;
      }
      return result;
    }

    const nested = buildNested(dictionary.allTokens);
    const json = JSON.stringify(nested, null, 2);

    return [
      header,
      `// Auto-generated by Style Dictionary — do not edit directly`,
      `// Edit source in packages/design-tokens/tokens/`,
      ``,
      `export const tokens = ${json};`,
      ``,
    ].join('\n');
  },
});

// CSS custom properties on :root
StyleDictionary.registerFormat({
  name: 'relay/css-vars',
  format: async ({ dictionary, file, options }) => {
    const header = await fileHeader({ file });
    const vars = dictionary.allTokens
      .map((token) => {
        const v = token.$value ?? token.value;
        let cssValue;
        if (typeof v === 'object' && v !== null && (token.$type === 'boxShadow' || token.type === 'boxShadow')) {
          // Format box-shadow
          cssValue = `${v.offsetX ?? '0px'} ${v.offsetY ?? '2px'} ${v.blur ?? '8px'} ${v.spread ?? '0px'} ${v.color ?? 'rgba(0,0,0,0.08)'}`;
        } else {
          cssValue = String(v);
        }
        return `  --${token.name}: ${cssValue};`;
      })
      .join('\n');

    return [
      header,
      `/* Auto-generated by Style Dictionary — do not edit directly */`,
      ``,
      `:root {`,
      vars,
      `}`,
      ``,
    ].join('\n');
  },
});

// Tailwind theme extension object
StyleDictionary.registerFormat({
  name: 'relay/tailwind-theme',
  format: async ({ dictionary, file, options }) => {
    const header = await fileHeader({ file });

    const colors = {};
    const spacing = {};
    const fontFamily = {};
    const borderRadius = {};
    const boxShadow = {};

    for (const token of dictionary.allTokens) {
      const v = token.$value ?? token.value;
      const type = token.$type ?? token.type;
      const p = token.path;

      if (p[0] === 'color') {
        // Flatten to kebab-joined path
        const key = p.slice(1).join('-');
        colors[key] = String(v);
      } else if (p[0] === 'spacing') {
        const key = p[1];
        spacing[key] = typeof v === 'number' ? `${v}px` : String(v);
      } else if (p[0] === 'typography' && p[1] === 'family') {
        const key = p[2];
        fontFamily[key] = [String(v), 'sans-serif'];
      } else if (p[0] === 'radius') {
        const key = p[1];
        borderRadius[key] = typeof v === 'number' ? `${v}px` : String(v);
      } else if (p[0] === 'shadow') {
        const key = p[1];
        if (typeof v === 'object' && v !== null) {
          boxShadow[key] = `${v.offsetX ?? '0px'} ${v.offsetY ?? '2px'} ${v.blur ?? '8px'} ${v.spread ?? '0px'} ${v.color ?? 'rgba(0,0,0,0.08)'}`;
        } else {
          boxShadow[key] = String(v);
        }
      }
    }

    const theme = { colors, spacing, fontFamily, borderRadius, boxShadow };
    const json = JSON.stringify(theme, null, 2);

    return [
      header,
      `// Auto-generated by Style Dictionary — do not edit directly`,
      `// Spread into your Tailwind config: theme: { extend: { ...tailwindTheme } }`,
      ``,
      `export const tailwindTheme = ${json};`,
      ``,
      `export default tailwindTheme;`,
      ``,
    ].join('\n');
  },
});


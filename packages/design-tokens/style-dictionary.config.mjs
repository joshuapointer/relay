// native/px-to-number: converts numeric px values to bare numbers for RN
StyleDictionary.registerTransform({
  name: 'native/px-to-number',
  type: 'value',
  filter: (token) => {
    const v = token.$value ?? token.value;
    return typeof v === 'string' && (v.endsWith('px') || v.endsWith('rem'));
  },
  transform: (token) => {
    const v = token.$value ?? token.value;
    if (typeof v === 'string' && v.endsWith('rem')) {
      return parseFloat(v) * 16;
    }
    return parseFloat(v);
  },
});

// native/shadow-to-rn: converts box-shadow object to RN shadow props
StyleDictionary.registerTransform({
  name: 'native/shadow-to-rn',
  type: 'value',
  filter: (token) => {
    const v = token.$value ?? token.value;
    return (
      (token.$type === 'boxShadow' || token.type === 'boxShadow') &&
      typeof v === 'object' &&
      v !== null
    );
  },
  transform: (token) => {
    const v = token.$value ?? token.value;
    return {
      shadowColor: v.color ?? 'rgba(0,0,0,0.1)',
      shadowOffset: {
        width: toNumber(v.offsetX ?? v.x ?? '0px'),
        height: toNumber(v.offsetY ?? v.y ?? '2px'),
      },
      shadowOpacity: 1,
      shadowRadius: toNumber(v.blur ?? '8px'),
      elevation: toNumber(v.blur ?? '8px') / 2,
    };
  },
});

